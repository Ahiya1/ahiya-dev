/** Builds the user message for a judging call. */
export function buildJudgeUserPrompt(args: {
  playerName: string;
  missionTitle: string;
  missionDescription: string;
  text?: string;
  hasImage: boolean;
}): string {
  const parts = [
    `מגיש/ה: ${args.playerName}`,
    `משימה: ${args.missionTitle}`,
    `תיאור המשימה: ${args.missionDescription}`,
  ];
  if (args.text) parts.push(`ההגשה (טקסט): ${args.text}`);
  if (args.hasImage) parts.push('ההגשה מצורפת כתמונה.');
  parts.push('החזירו את פסקי הדין של שלושת השופטים.');
  return parts.join('\n');
}
